; Hour
h0 = 1
h1 = 9

; Min
m0 = 4
m1 = 0

setup:
	; Status bits
	rtw #0x0f,#0x04,#0x01

	;Hour
	rtw #0x2,#h0,#h1

	;Min
	rtw #0x1,#m0,#m1

	;Sec
	rtw #0x0,#0,#0

display:
	; Hour
	rtr #0x02,0x40,0x41
	jsr calc
	out #0x04

	; Min
	rtr #0x01,0x40,0x41
	jsr calc
	out #0x0

	jmp display

calc:
	lda 0x40
	asl
	asl
	asl
	add 0x40
	add 0x40
	add 0x41
	rts